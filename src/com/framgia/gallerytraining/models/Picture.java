package com.framgia.gallerytraining.models;

public class Picture {

	private long idPicture;
	private String namePicture;
	private String typePicture;
	private int sizePicture;
	private String datePicture;
	private String dataPicture;
	private String srcPicture;

	public long getIdPicture() {
		return idPicture;
	}

	public void setIdPicture(long idPicture) {
		this.idPicture = idPicture;
	}

	public String getNamePicture() {
		return namePicture;
	}

	public void setNamePicture(String namePicture) {
		this.namePicture = namePicture;
	}

	public String getTypePicture() {
		return typePicture;
	}

	public void setTypePicture(String typePicture) {
		this.typePicture = typePicture;
	}

	public int getSizePicture() {
		return sizePicture;
	}

	public void setSizePicture(int sizePicture) {
		this.sizePicture = sizePicture;
	}

	public String getDatePicture() {
		return datePicture;
	}

	public void setDatePicture(String datePicture) {
		this.datePicture = datePicture;
	}

	public String getDataPicture() {
		return dataPicture;
	}

	public void setDataPicture(String dataPicture) {
		this.dataPicture = dataPicture;
	}

	public String getSrcPicture() {
		return srcPicture;
	}

	public void setSrcPicture(String srcPicture) {
		this.srcPicture = srcPicture;
	}

}
